# =============================================================================
# Modelling of RC-circuit
# =============================================================================
import matplotlib.pyplot as plt
import numpy as np

# =============================================================================
# Voltage drop across capacitor as a function of time
# =============================================================================

R = 4732
C = 98.98 * 10**(-9)
tau = R*C
V0 = 2

t = np.linspace(0, 10*tau, 1000)

V_C = np.exp(-t/tau)*V0 - V0/2

# =============================================================================
# Square wave
# =============================================================================

t_2 = np.linspace(-1, 1, 1000)

def wave(t):
    if t < 0:
        return V0/2
    else:
        return -V0/2


wave = np.vectorize(wave(t_2))

# =============================================================================
# Plotting
# =============================================================================

plt.plot(t, V_C, 'r-', t, wave, 'b-')
